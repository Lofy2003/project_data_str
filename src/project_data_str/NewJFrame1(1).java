    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double tot1 = (double) mid.getValue();
        double tot2 = (double) final_exam.getValue();
        s = new Student(id.getText(), fName.getText(), lName.getText(), tot1, tot2, total.getText(), estimation.getText());
        n = new Node(s, null, null);
        if (i == 0) {
            Project_data_str.l.insertFirst(n);

        } else if (i == 1) {
            Project_data_str.l.insertSecond(n);

        } else {
            Project_data_str.l.insertLast(n);
        }

        if (c == null) {
            c = new NewJFrame();
        }
        c.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

