    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        double tot1 = (double) mid.getValue();
        double tot2 = (double) final_exam.getValue();
        double tot = tot1 + tot2;
        String total_sum = String.valueOf(tot);
        total.setText(total_sum);

        if (tot < 60) {
            estimation.setText("راسب");
        } else if (tot >= 60 && tot < 70) {
            estimation.setText("مقبو ");
        } else if (tot >= 70 && tot < 80) {
            estimation.setText("جيد ");
        } else if (tot >= 80 && tot < 90) {
            estimation.setText("جيد جدا");
        } else if (tot >= 90) {
            estimation.setText("ممتاز ");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

